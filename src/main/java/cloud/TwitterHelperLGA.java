	public static Map<String,List<String>> getLGA(){
		Map<String,List<String>> result = new HashMap<String,List<String>>();
		String currentLGA = LGA_SUBURB[0][0].trim().toLowerCase();
		ArrayList<String> subs = new ArrayList<String>();
		for(String[] lga_sub: LGA_SUBURB){
		
			if(!lga_sub[0].equals(currentLGA)){
				result.put(currentLGA, subs);
				currentLGA = lga_sub[0];
				subs = new ArrayList<String>();
			}
			subs.add(lga_sub[1]);
		}
		result.put(currentLGA, subs);
		return result;
	}
	
